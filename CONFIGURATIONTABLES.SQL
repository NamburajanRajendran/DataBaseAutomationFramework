--CREATE A TABLE TO STORE TEST CASE DETAILS AND QUERY
CREATE TABLE TESTCASE_DATALOAD
(
TEST_CASE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
TEST_CASE_NAME VARCHAR2(500) NOT NULL,
TEST_CASE_DESCRIPTION VARCHAR2(5000) NOT NULL,
PASSED_DESCRIPTION VARCHAR2(5000) NOT NULL,
FAILED_DESCRIPTION VARCHAR2(5000) NOT NULL,
TEST_SCRIPT CLOB,
IS_TC_ENABLED VARCHAR2(200),
TEST_GROUP VARCHAR2(500),
UPDATED_BY VARCHAR2(100) DEFAULT 'NAMBURAJAN',
UPDATE_ON DATE DEFAULT SYSDATE
);

--CREATE A TABLE TO LOG TEST EXECUTION DETAILS
CREATE TABLE TESTCASE_DATALOAD_LOG
(
PROCESSING_DATE DATE DEFAULT SYSTIMESTAMP,
TEST_CASE_ID NUMBER,
TEST_CASE_NAME VARCHAR2(500) NOT NULL,
EXPECTED_RESULT VARCHAR2(5000) NOT NULL,
ACTUAL_RESULT VARCHAR2(5000) NOT NULL,
TEST_STATUS VARCHAR2(500) NOT NULL,
TEST_GROUP VARCHAR2(500),
UPDATED_BY VARCHAR2(100),
UPDATE_ON DATE DEFAULT SYSTIMESTAMP
);